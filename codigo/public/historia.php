    <h1>Política de Pricacidade</h1>
    <br>
    <p>A sua privacidade é muito importante para nós! Por isso, nesta Política de Privacidade explicamos como coletamos, usamos e protegemos as suas informações ao visitar o site da nossa hamburgueria.</p>
    <br>
    <p>1. Coleta de Informações</p>

    <p>Coletamos informações pessoais apenas quando necessário, como:</p>

    <ul>
        <li>Nome</li>
        <li>E-mail</li>
        <li>Telefone</li>
        <li>Informações para pagamento (em caso de pedidos online)</li>
        <li>Preferências de contato</li>
    </ul>

    <p>Também coletamos dados de navegação, como cookies e endereço IP, para melhorar sua experiência no site.</p>
    <br>
    <p>2. Uso das Informações</p>

    <p>As informações coletadas são utilizadas para:</p>
    
    <ul>
        <li>Processar pedidos e reservas</li>
        <li>Personalizar sua experiência</li>
        <li>Enviar novidades, promoções e comunicações relevantes (com o seu consentimento)</li>
        <li>Melhorar nossos serviços e atendimento</li>
    </ul>
    <br>
    <p>3. Compartilhamento de Dados</p>

    <p>Não vendemos nem compartilhamos suas informações pessoais com terceiros, exceto quando necessário para:</p>

    <ul>
        <li>Processamento de pagamentos</li>
        <li>Cumprimento de obrigações legais</li>
        <li>Prestação de serviços essenciais ao funcionamento do site</li>
    </ul>
    <br>
    <p>4. Segurança</p>

    <p>Adotamos medidas de segurança técnicas e administrativas para proteger seus dados contra acessos não autorizados, vazamentos ou qualquer forma de uso indevido.</p>
    <br>
    <p>5. Seus Direitos</p>

    <p>Você pode, a qualquer momento:</p>

    <ul>
        <li>Solicitar acesso aos seus dados</li>
        <li>Corrigir ou atualizar informações</li>
        <li>Solicitar a exclusão dos seus dados</li>
        <li>Optar por não receber nossas comunicações</li>
    </ul>

    <p>Para isso, entre em contato conosco através dos canais disponíveis no site.</p>
    <br>
    <p>6. Cookies</p>

    <p>Utilizamos cookies para melhorar a navegação e oferecer conteúdos personalizados. Você pode gerenciar ou desativar os cookies nas configurações do seu navegador.</p>
    <br>
    <p>7. Alterações na Política</p>

    <p>Podemos atualizar esta Política de Privacidade periodicamente. Recomendamos que você consulte esta página regularmente para estar sempre informado sobre eventuais mudanças.</p>
